					<h1>CFA Institute Research Challenge</h1>
					
						<h2> Introduction</h2>
							<p>The CFA Institute Research Challenge® is sponsored by CFA Institute and locally hosted by the New York 
							Society of Security Analysts. The CFA Institute Research Challenge is an educational initiative in which leading 
							industry professionals teach business and finance students how to research and report on a publicly-traded company. 
							NYSSA originated the competition thirteen years ago (as the “Investment Research Challenge”) as a means to promote best practices 
							in research among the next generation of analysts.</p>

							<p>The Research Challenge spans one academic year, and consists of the following components:</p>
								<ul>
									<li style = "list-style-type: disc" class = "limargin">
										<p class = "pmargin"><strong>Training in Research and Report Writing:</strong>
										Students receive training by attending NYSSA-sponsored seminars on Insider Tools for Analysts, 
										Presentation Skills, Report Writing, and Ethics. Seminar instructors are selected by NYSSA.</p>
									</li>
							
									<li style = "list-style-type: disc" class = "limargin">
										<p class = "pmargin"><strong>Analysis of a Public Company: </strong>
										Teams research the same public company, selected by NYSSA, including a question-and-answer session with company management.</p>
									</li>
									
									<li style = "list-style-type: disc" class = "limargin">
										<p class = "pmargin"><strong>Mentoring By a Professional Research Analyst: </strong>
										Each team is mentored by a professional research analyst, who reviews and critiques the team's report.</p>
									</li>
							      
								    <li style = "list-style-type: disc" class = "limargin">
										<p class = "pmargin"><strong>Assessment of Written Reports:</strong> 
										A group of volunteer graders selected from NYSSA’s membership assess the written research reports.</p>
									</li>
							
									<li style = "list-style-type: disc" class = "limargin">
										<p class = "pmargin"><strong>First-Round Presentation to High-Profile Wall Street Experts:</strong>
											All teams present their research to Wall Street experts.
											The experts choose the “final four” who have the strongest research to present their final report.</p></li>
							
									<li style = "list-style-type: disc" class = "limargin">
									<p class = "pmargin"><strong>Final-Four Presentation to a High-Profile Wall Street Experts:</strong>
									The “final four” present their research to a panel of respected Wall Street experts.
										The winning team is selected based on the combined scores for the written report and the presentation.</p></li>
								</ul>

